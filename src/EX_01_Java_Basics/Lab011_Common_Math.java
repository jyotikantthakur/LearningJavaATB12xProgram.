package EX_01_Java_Basics;

public class Lab011_Common_Math {
    public static void main(String[] args) {
        System.out.println(2+2);
        System.out.println(2*2);
        System.out.println(2/2);
        System.out.println(2-2);
        System.out.println(3/2); // // println -> Only Integer part -> No Decimal
        System.out.println("2+2"); // // println -> Double inverted will print the operation as it is
        System.out.println('2+2'); // // println -> Single inverted will not execute the operation (Error)
    }
}
